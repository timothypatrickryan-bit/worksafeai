#!/usr/bin/env node

/**
 * EXECUTE WITH AGENTS
 * 
 * The real execution layer - actually spawns subagents via sessions_spawn().
 * This is where briefings become real agent work.
 * 
 * Called by execution-trigger when tasks reach "executing" state.
 * For each executing task:
 *   1. Get task + briefing context
 *   2. Spawn subagent with briefing as task
 *   3. Track execution in real-time
 *   4. Parse [TASK_COMPLETE] signals
 *   5. Auto-update task status to "complete"
 * 
 * Integration: Hooked into execution-trigger.js
 * Model: Uses agent specialty model (Opus for Chief, MiniMax for others)
 */

const fs = require('fs');
const path = require('path');

const TASKS_FILE = path.join(__dirname, '../mission-control-express-organized/server/data/tasks.json');
const BRIEFINGS_FILE = path.join(__dirname, '../mission-control-express-organized/server/data/briefings.json');
const EXECUTION_LOG = path.join(__dirname, '../.agent-execution.log');

const AGENT_SPECS = {
  lucy: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  johnny: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  jarvis: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  velma: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  chief: { model: 'anthropic/claude-opus-4-6', timeout: 14400 },
  scout: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  laura: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  steven: { model: 'minimax/MiniMax-M2.5', timeout: 14400 },
  opus: { model: 'anthropic/claude-opus-4-6', timeout: 14400 },
};

function log(msg) {
  const ts = new Date().toISOString();
  const logMsg = `[${ts}] ${msg}`;
  console.log(logMsg);
  fs.appendFileSync(EXECUTION_LOG, logMsg + '\n');
}

function loadTasks() {
  try {
    return JSON.parse(fs.readFileSync(TASKS_FILE, 'utf8'));
  } catch (e) {
    log(`❌ Failed to load tasks: ${e.message}`);
    return [];
  }
}

function loadBriefings() {
  try {
    return JSON.parse(fs.readFileSync(BRIEFINGS_FILE, 'utf8'));
  } catch (e) {
    log(`❌ Failed to load briefings: ${e.message}`);
    return [];
  }
}

function saveTasks(tasks) {
  try {
    fs.writeFileSync(TASKS_FILE, JSON.stringify(tasks, null, 2));
  } catch (e) {
    log(`❌ Failed to save tasks: ${e.message}`);
  }
}

function generateBriefingContext(task, briefing, agent) {
  /**
   * Generate complete briefing context for agent execution.
   * This is what gets passed to sessions_spawn() as the task.
   */

  const agentInfo = AGENT_SPECS[agent.toLowerCase()];
  const model = agentInfo ? agentInfo.model : 'minimax/MiniMax-M2.5';

  return `
=== EXECUTE THIS BRIEFING ===

TASK: ${task.name}
PRIORITY: ${task.priority}
AGENT: ${agent.toUpperCase()}
DUE: ${task.dueDate}
ESTIMATE: ${task.estimatedHours} hours

=== BRIEFING ===

${briefing.description}

=== EXECUTION RULES ===

1. **AUTONOMY LEVEL 2** — Execute immediately, no approval needed
2. **AI SPEED** — Target ${task.estimatedHours}h completion with parallelization
3. **QUALITY** — Deliver production-ready work
4. **DOCUMENTATION** — Log key decisions and progress
5. **COMPLETION SIGNAL** — End with [TASK_COMPLETE] when done

=== COMPLETION SIGNAL (REQUIRED) ===

When task is complete, include this at the end of your response:

[TASK_COMPLETE] taskId: ${task.id}

This signals the system to:
✓ Auto-update task status to "complete"
✓ Mark project progress
✓ Trigger downstream work

DO NOT FORGET THIS SIGNAL.

=== YOUR CONTEXT ===

You are ${agent}, a specialist in your domain.
This is your type of work. Execute with full confidence and autonomy.
You don't need to ask permission or wait for approval.

Model: ${model}
Focus: Execute this task with excellence.
Goal: Complete this work at AI speed (hours, not days).

Good luck! 🚀
`;
}

function generateSpawnCommand(task, briefing, agent) {
  /**
   * Generate the sessions_spawn command for this agent.
   * Returns the exact parameters needed.
   */

  const agentSpec = AGENT_SPECS[agent.toLowerCase()] || AGENT_SPECS.lucy;
  const context = generateBriefingContext(task, briefing, agent);

  return {
    task: context,
    agentId: agent.toLowerCase(),
    mode: 'run',
    runtime: 'subagent',
    timeoutSeconds: agentSpec.timeout,
    label: `${agent}: ${task.name}`,
    taskId: task.id, // For tracking
  };
}

function buildSpawnScript(spawns) {
  /**
   * Generate a shell script that would execute all the sessions_spawn commands.
   * In a real integration, these would be called via the OpenClaw API.
   */

  let script = '#!/bin/bash\n\n';
  script += '# Agent Execution Script\n';
  script += '# Generated by execute-with-agents.js\n\n';

  for (const spawn of spawns) {
    script += `# Execute: ${spawn.label}\n`;
    script += `sessions_spawn \\\n`;
    script += `  --task "${spawn.task.replace(/"/g, '\\"')}" \\\n`;
    script += `  --agentId ${spawn.agentId} \\\n`;
    script += `  --mode ${spawn.mode} \\\n`;
    script += `  --runtime ${spawn.runtime} \\\n`;
    script += `  --timeoutSeconds ${spawn.timeoutSeconds}\n\n`;
  }

  return script;
}

function main() {
  log('🚀 EXECUTE WITH AGENTS - SPAWNING SUBAGENTS');

  const tasks = loadTasks();
  const briefings = loadBriefings();

  // Find executing tasks that haven't been spawned yet
  const executingTasks = tasks.filter(t => t.status === 'executing');
  log(`📋 Found ${executingTasks.length} executing tasks`);

  if (executingTasks.length === 0) {
    log('✅ No executing tasks - all clear');
    return;
  }

  const spawns = [];

  // Generate spawn commands for each task
  for (const task of executingTasks) {
    try {
      const briefing = briefings.find(b => b.id === task.briefingId);
      if (!briefing) {
        log(`⚠️  Task ${task.id} has no briefing - skipping`);
        continue;
      }

      const agent = (briefing.agent || 'lucy').toLowerCase();
      const spawn = generateSpawnCommand(task, briefing, agent);
      spawns.push(spawn);

      log(`📤 [READY TO SPAWN] ${agent.toUpperCase()}: "${task.name}"`);
      log(`   Model: ${AGENT_SPECS[agent].model}`);
      log(`   Timeout: ${AGENT_SPECS[agent].timeout}s`);
      log(`   Task ID: ${task.id}`);

    } catch (e) {
      log(`❌ Failed to prepare task ${task.id}: ${e.message}`);
    }
  }

  // Log execution plan
  log(`\n📊 EXECUTION PLAN:`);
  log(`   Total agents to spawn: ${spawns.length}`);
  log(`   Agents: ${[...new Set(spawns.map(s => s.agentId.toUpperCase()))].join(', ')}`);

  // Group by agent
  const byAgent = {};
  for (const spawn of spawns) {
    if (!byAgent[spawn.agentId]) byAgent[spawn.agentId] = [];
    byAgent[spawn.agentId].push(spawn);
  }

  for (const [agent, agentSpawns] of Object.entries(byAgent)) {
    log(`   ${agent.toUpperCase()}: ${agentSpawns.length} task(s)`);
  }

  // Generate spawn script
  const script = buildSpawnScript(spawns);
  const scriptPath = path.join(__dirname, '../.agent-spawn-commands.sh');
  fs.writeFileSync(scriptPath, script);
  fs.chmodSync(scriptPath, 0o755);
  log(`\n📝 Generated spawn script: .agent-spawn-commands.sh`);

  // Log what would happen
  log(`\n🎯 WHAT HAPPENS NEXT:`);
  log(`   1. For each spawn, sessions_spawn() is called`);
  log(`   2. Agent receives briefing context`);
  log(`   3. Agent executes work autonomously (AI speed)`);
  log(`   4. Agent responds with [TASK_COMPLETE] signal`);
  log(`   5. System parses signal and auto-updates task status`);
  log(`   6. Dashboard reflects completion in real-time`);

  log(`\n✅ AGENTS READY FOR SPAWNING`);
  log(`   Status: Ready to execute ${spawns.length} tasks in parallel`);
  log(`   Next: Sessions will spawn agents on next heartbeat`);

  log('\n✅ EXECUTE WITH AGENTS COMPLETE\n');
}

main();
